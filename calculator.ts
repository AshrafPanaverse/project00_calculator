const add= (num1:any,num2:any)=>{
    return (num1+num2);
}

const subtract=(num1:any,num2:any)=>{
    return (num1-num2);
}

const multiply=(num1:any,num2:any)=>{
    return (num1*num2);
}

const division=(num1:any,num2:any)=>{
    return (num1/num2);
}

export default {add,subtract,multiply,division};